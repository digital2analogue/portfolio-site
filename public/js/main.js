(() => {
  // <stdin>
  console.log("This site was generated by Hugo.");
  (function() {
    const currentTheme = localStorage.getItem("theme") || "light";
    document.documentElement.setAttribute("data-theme", currentTheme);
  })();
  document.addEventListener("DOMContentLoaded", function() {
    const themeToggle = document.getElementById("theme-toggle");
    if (themeToggle) {
      themeToggle.addEventListener("click", function() {
        const htmlElement = document.documentElement;
        const currentTheme = htmlElement.getAttribute("data-theme");
        const newTheme = currentTheme === "dark" ? "light" : "dark";
        htmlElement.setAttribute("data-theme", newTheme);
        localStorage.setItem("theme", newTheme);
      });
    }
    const navbarToggle = document.getElementById("navbar-toggle");
    const navbarMenu = document.getElementById("navbar-menu");
    const body = document.body;
    if (navbarToggle && navbarMenu) {
      navbarToggle.addEventListener("click", function() {
        navbarToggle.classList.toggle("active");
        navbarMenu.classList.toggle("active");
        body.classList.toggle("menu-open");
      });
      body.addEventListener("click", function(e) {
        if (navbarMenu.classList.contains("active") && !navbarMenu.contains(e.target) && !navbarToggle.contains(e.target)) {
          navbarToggle.classList.remove("active");
          navbarMenu.classList.remove("active");
          body.classList.remove("menu-open");
        }
      });
      const menuLinks = navbarMenu.querySelectorAll("a");
      menuLinks.forEach((link) => {
        link.addEventListener("click", function() {
          navbarToggle.classList.remove("active");
          navbarMenu.classList.remove("active");
          body.classList.remove("menu-open");
        });
      });
    }
  });
  document.addEventListener("DOMContentLoaded", function() {
    const carousel = document.querySelector(".featured-carousel");
    const prevBtn = document.querySelector(".carousel-btn-prev");
    const nextBtn = document.querySelector(".carousel-btn-next");
    if (!carousel || !prevBtn || !nextBtn) return;
    const cards = carousel.querySelectorAll(".featured-card");
    const cardCount = cards.length;
    let currentIndex = 0;
    function updateCarousel() {
      const cardWidth = cards[0].offsetWidth;
      const gap = 20;
      const offset = currentIndex * (cardWidth + gap);
      carousel.style.transform = `translateX(-${offset}px)`;
      prevBtn.disabled = currentIndex === 0;
      nextBtn.disabled = currentIndex >= cardCount - 3;
    }
    prevBtn.addEventListener("click", function() {
      if (currentIndex > 0) {
        currentIndex--;
        updateCarousel();
      }
    });
    nextBtn.addEventListener("click", function() {
      if (currentIndex < cardCount - 3) {
        currentIndex++;
        updateCarousel();
      }
    });
    window.addEventListener("resize", updateCarousel);
    updateCarousel();
  });
})();
